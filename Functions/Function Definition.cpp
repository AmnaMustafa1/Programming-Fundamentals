//The function definition contains the body of the function, where the actual logic is implemented.
//Explanation: The function sum calculates the sum of two integers in its body.
#include <iostream>
using namespace std;

int sum(int a, int b) {  // Function definition
    return a + b;
}

int main() {
    cout << "Sum of 5 and 7: " << sum(5, 7) << endl;
    return 0;
}

